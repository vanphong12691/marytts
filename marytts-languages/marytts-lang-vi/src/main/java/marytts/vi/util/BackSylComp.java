package marytts.vi.util;

public class BackSylComp {
	
	public int num;
	public String s;
	
	public BackSylComp(int num, String s) {
        this.num = num;
        this.s = s;
    }

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	
}
